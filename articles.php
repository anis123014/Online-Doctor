	   <h2> <font color=red>ড. তাহেরের জীবন্ত রোবট বিপ্লব </font></h1> <br>
	  
	   <p>"২০১৬ সালে প্রযুক্তিতে সবচেয়ে বড় বিপ্লবটা কী ঘটবে?" ----
‘আপনার রক্তনালিতে ঘুরে বেড়াবে জীবন্ত রোবট—শিরায়-ধমনিতে, ক্যানসার কোষ খুঁজে বেড়াবে, ক্যানসার কোষকে ধ্বংস করবে।’
নিউইয়র্ক টাইমস-এর ৩১ ডিসেম্বর ২০১৫ সংখ্যায় এ প্রশ্ন করেছেন এবং এর এই উত্তর দিয়েছেন নিকোলাস ক্রিস্টোফ। দু-দুবার পুলিৎজার পুরস্কার পাওয়া এই কলাম লেখক আমেরিকার অগ্রগণ্য চিন্তাবিদ হিসেবেও খ্যাতিমান।
তা পড়ে বাংলাদেশের মানুষের নববর্ষ হয়ে উঠেছে গৌরবের। কারণ এই যে জীবন্ত রোবট, ক্ষুদ্রাতিক্ষুদ্র, এটা নিয়ে গবেষণা করছেন যিনি, তিনি আমাদের ঢাকার সন্তান। অধ্যাপক তাহের এ সাইফ। বর্তমানে আমেরিকার ইলিনয় বিশ্ববিদ্যালয়ে অধ্যাপনা ও গবেষণা করছেন। মাধ্যমিক পরীক্ষা দিয়েছেন চট্টগ্রামের সেন্ট প্লাসিডস স্কুল থেকে (১৯৭৭), উচ্চমাধ্যমিক ঢাকা কলেজ। বাংলাদেশ প্রকৌশল বিশ্ববিদ্যালয়ের পুরকৌশল বিভাগ থেকে স্নাতক। ড. জামিলুর রেজা চৌধুরীর অধীনে স্নাতকোত্তরের ছাত্র ছিলেন। তাঁকে নিয়ে প্রথম আলোতে নুরুন্নবী চৌধুরী লিখেছিলেন ‘আমিই বাংলাদেশ’ শিরোনামের প্রতিবেদন, ‘বিশ্বের সবচেয়ে ক্ষুদ্র রোবট’ (৫ আগস্ট ২০১৪)।
নিউইয়র্ক টাইমস-এর এই প্রতিবেদনটা চোখেই পড়েনি অধ্যাপক তাহেরের। </p> 
